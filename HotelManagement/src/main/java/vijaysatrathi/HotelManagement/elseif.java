package vijaysatrathi.HotelManagement;

public class elseif {
	public static void main(String[] args) {
		String name="raja";
		if(name=="vijay")
		{
			System.out.println("name is vijay");
		}
		else if(name=="manoj")
		{
			System.out.println("name is manoj");
		}
		else if(name=="kishore")
		{
			System.out.println("name is kishore");
		}
		else
		{
			System.out.println(name);
		}
		
	}

}
